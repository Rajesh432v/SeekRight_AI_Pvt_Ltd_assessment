// Example using var
var x = 10;
x += 5; // Increment x by 5
console.log(`Value of x (var): ${x}`);

// Example using let
let y = 20;
y *= 2; // Multiply y by 2
console.log(`Value of y (let): ${y}`);

// Example using const
const z = 30;
// z = z + 10; // This line will throw an error because z is a constant
console.log(`Value of z (const): ${z}`);
